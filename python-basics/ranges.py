# Ponto de parada
for num in range(11):
    print(f'{num} ', end='')

print('\n')
# Ponto de partida (1 item)
for num in range(1, 11):
    print(f'{num} ', end='')

print('\n')
# Com passo (3 item)
for num in range(5, 50, 2):
    print(f'{num} ', end='')

print('\n')
# Inverso
for num in range(10, -1, -1):
    print(f'{num} ', end='')

print('\n')